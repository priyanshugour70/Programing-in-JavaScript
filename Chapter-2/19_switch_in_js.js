

// switch in JavaScript

let num = 3 ;

switch(num){
    case 0 :{
        console.log("Your number is zero ..");
        break ;
    }
    case 1 :{
        console.log("Your number is one ..");
        break ;
    }
    case 2 :{
        console.log("Your number is two ..");
        break ;
    }
    case 3 :{
        console.log("Your number is three ...");
        break ;
    }
    default:
        console.log("your number is greater then three ....");
}