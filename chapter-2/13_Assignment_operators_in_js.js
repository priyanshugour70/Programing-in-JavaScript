
// assignment operators in javascript .

let a = 10 ;

a += 5 // same as a = a + 5 
console.log("a += 5 : ", a)

a -= 5 // same as a = a - 5 
console.log("a -= 5 : ", a)

a *= 5 // same as a = a * 5 
console.log("a *= 5 :", a)

a /= 5 // same as a = a/5
console.log("a /= 5 :", a)

a %= 4 // same as a = a % 4
console.log("a %= 4 : ", a)

a **= 5 // same as a = a ** 5
console.log("a **= 5 : ", a)